const normalizeName = (value = "") =>
  String(value)
    .toLowerCase()
    .replace(/^(xã|phường|thị trấn|thành phố|thị xã|huyện|tp|tx)\s+/i, "")
    .replace(/^(xa|phuong|thi tran|thanh pho|thi xa|huyen|tp|tx)\s+/i, "")
    .replace(/\s+/g, " ")
    .trim();

const displayNameCandidates = (place = {}) => {
  const items = [];

  if (place.name) items.push(place.name);
  if (place.title) items.push(place.title);
  if (place.oldDistrict) items.push(place.oldDistrict);
  if (place.regionLabel) items.push(place.regionLabel);
  if (place.regionSubLabel) items.push(place.regionSubLabel);
  if (place.description) items.push(place.description);

  if (Array.isArray(place.formerUnits)) items.push(...place.formerUnits);
  if (Array.isArray(place.oldUnits)) items.push(...place.oldUnits);
  if (Array.isArray(place.mergedFrom)) items.push(...place.mergedFrom);

  return [...new Set(items.filter(Boolean))];
};

const matchStage = (place, stage) => {
  const candidates = displayNameCandidates(place).map(normalizeName);
  const matchTerms = (stage.match || []).map(normalizeName);

  return candidates.some((candidate) =>
    matchTerms.some((term) => {
      if (!candidate || !term) return false;
      return candidate === term;
    })
  );
};

export function getAdministrativeTimeline(place = {}) {
  const stages = [];
  const seen = new Set();

  adminHistory2024.forEach((stage) => {
    if (!matchStage(place, stage)) return;

    const key = `${stage.id}-${stage.description}`;
    if (seen.has(key)) return;

    seen.add(key);
    stages.push({
      year: stage.year,
      title: stage.title,
      description: stage.description,
      source: stage.source,
      relatedUnit: stage.id
    });
  });

  return stages;
}


