export function findPlaceBySlug(slug) {
  return PLACES.find((place) => place.slug === slug) || PLACES[0];
}

export function normalizeText(value = "") {
  return value
    .toString()
    .toLowerCase()
    .normalize("NFD")
    .replace(/[̀-ͯ]/g, "")
    .replace(/đ/g, "d");
}

export function searchPlaces(query, places = PLACES) {
  const q = normalizeText(query);
  if (!q) return places;
  return places.filter((place) => {
    const haystack = normalizeText([
      place.name,
      place.oldDistrict,
      place.regionLabel,
      ...(place.formerUnits || []),
    ].join(" "));
    return haystack.includes(q);
  });
}
